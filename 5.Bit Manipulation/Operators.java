public class Operators {
    public static void main(String[] args){
        int a = 5;
        int b = 6;
        System.out.println(a&b);  //And Operator
        System.out.println(a|b);  // Or Operator
        System.out.println(a^b);  //XOR Operator
        System.out.println(~a);   //Not Operator
        System.out.println(a<<1);  //Left Shift Operator
        System.out.println(a>>1);   //Right Shift Operator
        System.out.println(a>>>1);  //Unsigned Right Shift Operator
    }
    
}
